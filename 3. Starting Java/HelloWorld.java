//the public keyword here is the access modifier
//so here public means this code can be accessed publicly
//the class keyword is used to define a class
public class HelloWorld {
    //public static.... args) this is a main method
    //method is a collection of statements that perform an operation
    //void is used because there is no return value
    //String[] args is a parameter
    //Also java is case sensitive so the below code should be as it is
    public static void main(String[] args) {
        System.out.println("Hello World");
    }
}
